 #include<iostream>
using namespace std;
int main()
{
    int i,n ;
    i=1;                 //initialization
    cout<<"\n enter the value of n: ";
    cin>>n;
    while (i<=n)          //condition
    {
        cout<<i<<"\n";   //function
        i=i+1;           //increment
    }

    return 0;
}


